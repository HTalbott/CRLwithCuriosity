#!/usr/bin/env python3
"""Emit a reward + success-rate results table (LaTeX + markdown) from the raw
CSV produced by fetch_report_data.py.

Aggregation: per-seed mean over the last LAST_N_EVALS checkpoints, then mean/std
across seeds (sample std, ddof=1). Best row is bolded.
"""
from __future__ import annotations
import sys
from pathlib import Path
import numpy as np
import pandas as pd

OUT_DIR = Path(__file__).resolve().parent
LAST_N_EVALS = 20

SPECS = {
    "ant_ball_70m": {
        "caption": "Ant\\_ball 70M: final eval reward and success rate across 4 seeds.",
        "label": "tab:ab70_results",
        "prefix": "ant_ball_70m",
        "order": ["Vanilla CRL", "CRL+EMA", "CRL+EMA+Goal (reg=1e-7)"],
    },
    "humanoid_50m": {
        "caption": "Humanoid 50M: final eval reward and success rate across 4 seeds.",
        "label": "tab:hu50_results",
        "prefix": "humanoid_50m",
        "order": ["Vanilla CRL", "CRL+EMA", "CRL+EMA+Goal (reg=1e-7)"],
    },
}

ACTIVE = "ant_ball_70m"
SPEC = SPECS[sys.argv[1] if len(sys.argv) > 1 else ACTIVE]


def variant_label_latex(v: str) -> str:
    return v.replace("reg=1e-7", "reg=$10^{-7}$").replace("_", "\\_")


def aggregate(raw: pd.DataFrame, metric: str):
    """Return dict variant -> (per_seed_means array, overall_mean, overall_std)."""
    out = {}
    for variant, df_v in raw.groupby("variant"):
        per_seed = []
        for _, df_s in df_v.groupby("seed"):
            last = df_s.sort_values("eval_idx").tail(LAST_N_EVALS)
            per_seed.append(last[metric].mean())
        per_seed = np.asarray(per_seed, dtype=float)
        out[variant] = (per_seed, float(per_seed.mean()), float(per_seed.std(ddof=1)))
    return out


def detect_success_scale(success_means: dict) -> str:
    """Return 'pct' if values look like 0–100, else 'frac' for 0–1."""
    maxv = max(m for (_, m, _) in success_means.values())
    return "pct" if maxv > 1.5 else "frac"


def fmt_reward(m, s):
    return f"${m:.2f} \\pm {s:.2f}$"


def fmt_success(m, s, scale):
    if scale == "pct":
        return f"${m:.1f} \\pm {s:.1f}$"
    return f"${m*100:.1f} \\pm {s*100:.1f}$"


def main():
    raw_path = OUT_DIR / f"{SPEC['prefix']}_raw.csv"
    if not raw_path.exists():
        sys.exit(f"missing {raw_path} — run fetch_report_data.py first")
    raw = pd.read_csv(raw_path)

    reward_agg = aggregate(raw, "eval/episode_reward")
    has_success = "eval/episode_success_any" in raw.columns and not raw["eval/episode_success_any"].isna().all()
    success_agg = aggregate(raw, "eval/episode_success_any") if has_success else {}
    scale = detect_success_scale(success_agg) if has_success else "frac"

    # Find best (by mean reward) to bold
    best_variant = max(SPEC["order"], key=lambda v: reward_agg[v][1])

    # Markdown preview
    md = [f"# {SPEC['prefix']} results\n",
          f"Reward = per-seed mean over last {LAST_N_EVALS} eval checkpoints, then mean ± std (ddof=1) across 4 seeds. "
          f"Success rate shown as {'percentage' if scale == 'pct' else 'fraction × 100 (percentage)'}.\n"]
    if has_success:
        md.append("| Variant | Reward | Success rate (%) |")
        md.append("|---|---|---|")
    else:
        md.append("| Variant | Reward |")
        md.append("|---|---|")
    for v in SPEC["order"]:
        _, r_m, r_s = reward_agg[v]
        marker = "**" if v == best_variant else ""
        r_cell = f"{marker}{r_m:.2f} ± {r_s:.2f}{marker}"
        if has_success:
            _, s_m, s_s = success_agg[v]
            if scale == "pct":
                s_cell = f"{marker}{s_m:.1f} ± {s_s:.1f}{marker}"
            else:
                s_cell = f"{marker}{s_m*100:.1f} ± {s_s*100:.1f}{marker}"
            md.append(f"| {marker}{v}{marker} | {r_cell} | {s_cell} |")
        else:
            md.append(f"| {marker}{v}{marker} | {r_cell} |")
    md_path = OUT_DIR / f"{SPEC['prefix']}_results.md"
    md_path.write_text("\n".join(md) + "\n")

    # LaTeX
    cols = "lcc" if has_success else "lc"
    hdr = "\\textbf{Variant} & \\textbf{Reward} & \\textbf{Success rate (\\%)} \\\\" if has_success \
          else "\\textbf{Variant} & \\textbf{Reward} \\\\"
    tex = [
        "% Auto-generated by results/report/write_results_table.py",
        "% Requires: \\usepackage{booktabs}",
        "\\begin{table}[t]",
        "\\centering",
        f"\\caption{{{SPEC['caption']}}}",
        f"\\label{{{SPEC['label']}}}",
        f"\\begin{{tabular}}{{{cols}}}",
        "\\toprule",
        hdr,
        "\\midrule",
    ]
    for v in SPEC["order"]:
        _, r_m, r_s = reward_agg[v]
        label = variant_label_latex(v)
        if v == best_variant:
            label = f"\\textbf{{{label}}}"
            r_cell = f"$\\mathbf{{{r_m:.2f} \\pm {r_s:.2f}}}$"
        else:
            r_cell = fmt_reward(r_m, r_s)
        if has_success:
            _, s_m, s_s = success_agg[v]
            if v == best_variant:
                if scale == "pct":
                    s_cell = f"$\\mathbf{{{s_m:.1f} \\pm {s_s:.1f}}}$"
                else:
                    s_cell = f"$\\mathbf{{{s_m*100:.1f} \\pm {s_s*100:.1f}}}$"
            else:
                s_cell = fmt_success(s_m, s_s, scale)
            tex.append(f"{label} & {r_cell} & {s_cell} \\\\")
        else:
            tex.append(f"{label} & {r_cell} \\\\")
    tex += [
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table}",
    ]
    tex_path = OUT_DIR / f"{SPEC['prefix']}_results.tex"
    tex_path.write_text("\n".join(tex) + "\n")

    print(f"wrote {md_path}")
    print(f"wrote {tex_path}")
    print()
    print(md_path.read_text())


if __name__ == "__main__":
    main()
